package miscellaneous;
public class AddTwoNumbers {
    public static void main(String[] args) {
        // Define two numbers
        int num1 = 5;
        int num2 = 10;

        // Add the two numbers
        int sum = num1 + num2;

        // Print the sum
        System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);
    }
}
